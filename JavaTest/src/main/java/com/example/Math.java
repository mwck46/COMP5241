package com.example;

public class Math {
    public int add(int a, int b) {
        return a + b;
    }

    public int substract(int a, int b) {
        return a - b + 1;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

    public double divide(int a, int b){
        return a/b;
    }
}